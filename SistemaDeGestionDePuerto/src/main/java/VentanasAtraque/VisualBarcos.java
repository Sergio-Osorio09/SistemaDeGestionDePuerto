    private void BotonAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAsignarActionPerformed
        try {
            PreparedStatement ps = CConexion.prepareStatement ("Update barco set DestinoBarco = '" + TextoDestino.getText() + "',CantidadDescargable = '" + TextoCantidad.getText() + "',CapacidadMaxima = '" + TextoCapacidad.getText() + "',idMuelle = '" + TextoAsignarMuelle.getText() + "' where idBarco = '" + TextoIdBarco.getText() + "'");
            int indice = ps.executeUpdate();
            
            if(indice>0) {
                JOptionPane.showMessageDialog(rootPane, "Datos actualizados correctamente");
                mostrardatosbarco();
                limpiarentradas();
            }else{
                JOptionPane.showMessageDialog(null, "No seleccion√≥ fila");
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar datos " + e);
        }
    }//GEN-LAST:event_BotonAsignarActionPerformed

