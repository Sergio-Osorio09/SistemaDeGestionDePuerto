    private void BotonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActualizarActionPerformed
        try {
            PreparedStatement ps = CConexion.prepareStatement ("Update muelle set EstadoDeMuelle = '" + ComboEstado.getSelectedItem() + "' where idMuelle = '" + TextoIdMuelle.getText() + "'");
            int indice = ps.executeUpdate();
            
            if(indice>0) {
                JOptionPane.showMessageDialog(rootPane, "Datos actualizados correctamente");
                mostrardatos();
            }else{
                JOptionPane.showMessageDialog(null, "No seleccion√≥ fila");
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar datos " + e);
        }
    }//GEN-LAST:event_BotonActualizarActionPerformed

