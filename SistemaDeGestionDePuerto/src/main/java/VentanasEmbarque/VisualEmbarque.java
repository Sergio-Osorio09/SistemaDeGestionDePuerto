    private void BotonContenedoresAlmacenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonContenedoresAlmacenActionPerformed
        try {
            ResultSet rs = null;
            PreparedStatement ps = null;

            Clases.CConexion objetoConexion = new Clases.CConexion();

            // La consulta SQL para seleccionar contenedores en función del ID de la zona de almacenamiento
            String consulta = "SELECT * FROM Contenedor WHERE idZonaDeAlmacenamiento = ?";
            ps = objetoConexion.estableceConexion().prepareStatement(consulta);
            
            ps.setInt(1,Integer.parseInt(IngresarIDAlmacen.getText()));

            rs = ps.executeQuery();

            ResultSetMetaData metaData = rs.getMetaData();
           
            int numColumnas = metaData.getColumnCount();

            // Utiliza un arreglo dinámico para almacenar los datos
            String[] data = new String[numColumnas];

            while(rs.next()){                  
                for (int i = 0; i < numColumnas; i++) {
                    data[i] = rs.getString(i + 1); // Indices comienzan desde 1 en ResultSet
                }
            modelo.addRow(data);
            }    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e.toString());
        } 
    }//GEN-LAST:event_BotonContenedoresAlmacenActionPerformed

